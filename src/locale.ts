export const SUPPORTED_LOCALES = [
  "en",
  "es",
  "fr",
  "de",
  "pt-BR",
  "hi",
  "ja",
  "zh-CN",
] as const satisfies readonly SupportedLocale[];

export const MESSAGE_CODES = Object.keys(en) as Array<keyof typeof en>;

const dictionaries: Record<SupportedLocale, Record<string, string>> = {
  en,
  es,
  fr,
  de,
  "pt-BR": ptBR,
  hi,
  ja,
  "zh-CN": zhCN,
};

export function resolveLocale(locale?: string): SupportedLocale {
  if (!locale) return "en";
  if (locale in dictionaries) return locale as SupportedLocale;
  const base = locale.split("-")[0];
  if (base && base in dictionaries) return base as SupportedLocale;
  return "en";
}

export function formatMessage(
  code: string,
  locale?: string,
  params?: MessageParams,
): string {
  const resolved = resolveLocale(locale);
  const english = dictionaries.en;
  const dictionary = dictionaries[resolved] ?? english;
  const template = dictionary[code] ?? english[code] ?? code;
  return template.replace(/\{([a-zA-Z0-9_]+)\}/g, (_, key: string) =>
    String(params?.[key] ?? ""),
  );
}

export function withMessages<T extends EmailIssue>(
  issues: T[],
  locale?: string,
): T[] {
  return issues.map((issue) => ({
    ...issue,
    message: formatMessage(issue.code, locale, issue.params),
  }));
}
